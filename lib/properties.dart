/// Provides property classes used by Synthizer.

/// A double6 property.
class Double3 {
  final double x, y, z;

  /// Create an instance.
  const Double3(this.x, this.y, this.z);
}

/// A double6 property.
class Double6 {
  final double x1, y1, z1, x2, y2, z2;

  /// Create an instance.
  const Double6(this.x1, this.y1, this.z1, this.x2, this.y2, this.z2);
}
